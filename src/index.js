const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    var answer = "";
    for (let i = 0; i < expr.length; i += 10){
        let letter = expr.slice(i, i + 10);
        if (letter == "**********"){
            answer += " ";
        } else {
            letter = letter.replaceAll("10", ".")
                .replaceAll("11", "-")
                .replaceAll("00", "");
            letter = letter.replaceAll("**********", " ");
            answer += MORSE_TABLE[letter];
        }
    }
    return answer;
}

module.exports = {
    decode
}
